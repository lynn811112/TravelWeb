package model;

public class ComImage {

	private int imgId;
	private int comId;
	private byte[] comImage;
	
	public int getImgId() {
		return imgId;
	}

	public void setImgId(int imgId) {
		this.imgId = imgId;
	}

	public int getComId() {
		return comId;
	}

	public void setComId(int comId) {
		this.comId = comId;
	}

	public byte[] getComImage() {
		return comImage;
	}

	public void setComImage(byte[] comImage) {
		this.comImage = comImage;
	}



}
